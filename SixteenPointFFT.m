function y = SixteenPointFFT(mat)
    mat = OddEvenDecomposer(mat) ; 
    twid1 = exp(-1j*2*pi/16) ;
    twid2 = twid1*twid1 ; twid3 = twid1*twid1*twid1 ;
    twid4 = twid1*twid1*twid1*twid1 ;
    twid5 = twid1*twid1*twid1*twid1*twid1 ;
    twid6 = twid1*twid1*twid1*twid1*twid1*twid1 ;
    twid7 = twid1*twid1*twid1*twid1*twid1*twid1*twid1 ;
    out1 = zeros(1,16) ; out2 = zeros(1,16) ; out3 = zeros(1,16) ;
    out4 = zeros(1,16) ;
    out1(1,1) = mat(1,1) + mat(1,2) ; out1(1,2) = mat(1,1) - mat(1,2) ;
    out1(1,3) = mat(1,3) + mat(1,4) ; out1(1,4) = twid4*(mat(1,3) - mat(1,4)) ;
    out1(1,5) = mat(1,5) + mat(1,6) ; out1(1,6) = mat(1,5) - mat(1,6) ;
    out1(1,7) = mat(1,7) + mat(1,8) ; out1(1,8) = twid4*(mat(1,7) - mat(1,8)) ;
    out1(1,9) = mat(1,9) + mat(1,10) ; out1(1,10) = mat(1,9) - mat(1,10) ;
    out1(1,11) = mat(1,11) + mat(1,12) ; out1(1,12) = twid4*(mat(1,11) - mat(1,12)) ;
    out1(1,13) = mat(1,13) + mat(1,14) ; out1(1,14) = mat(1,13) - mat(1,14) ;
    out1(1,15) = mat(1,15) + mat(1,16) ; out1(1,16) = twid4*(mat(1,15) - mat(1,16)) ;
    out2(1,1) = out1(1,1) + out1(1,3) ; out2(1,2) = out1(1,2) + out1(1,4) ;
    out2(1,3) = out1(1,1) - out1(1,3) ; out2(1,4) = out1(1,2) - out1(1,4) ;
    out2(1,5) = out1(1,5) + out1(1,7) ; out2(1,6) = twid2*(out1(1,6) + out1(1,8)) ;
    out2(1,7) = twid4*(out1(1,5) - out1(1,7)) ; out2(1,8) = twid6* (out1(1,6) - out1(1,8)) ;
    out2(1,9) = out1(1,9) + out1(1,11) ; out2(1,10) = out1(1,10) + out1(1,12) ;
    out2(1,11) = out1(1,9) - out1(1,11) ; out2(1,12) = out1(1,10) - out1(1,12) ;
    out2(1,13) = out1(1,13) + out1(1,15) ; out2(1,14) = twid2*(out1(1,14) + out1(1,16)) ;
    out2(1,15) = twid4*(out1(1,13) - out1(1,15)) ; out2(1,16) = twid6*(out1(1,14) - out1(1,16)) ;
    out3(1,1) = out2(1,1) + out2(1,5) ; out3(1,2) = out2(1,2) + out2(1,6) ;
    out3(1,3) = out2(1,3) + out2(1,7) ; out3(1,4) = out2(1,4) + out2(1,8) ;
    out3(1,5) = out2(1,1) - out2(1,5) ; out3(1,6) = out2(1,2) - out2(1,6) ;
    out3(1,7) = out2(1,3) - out2(1,7) ; out3(1,8) = out2(1,4) - out2(1,8) ;
    out3(1,9) = out2(1,9) + out2(1,13) ; out3(1,10) = twid1*(out2(1,10) + out2(1,14)) ;
    out3(1,11) = twid2*(out2(1,11) + out2(1,15)) ; out3(1,12) = twid3*(out2(1,12) + out2(1,16)) ;
    out3(1,13) = twid4*(out2(1,9) - out2(1,13)) ; out3(1,14) = twid5*(out2(1,10) - out2(1,14)) ;
    out3(1,15) = twid6*(out2(1,11) - out2(1,15)) ; out3(1,16) = twid7*(out2(1,12) - out2(1,16)) ;
    out4(1,1) = out3(1,1) + out3(1,9) ; out4(1,2) = out3(1,2) + out3(1,10) ;
    out4(1,3) = out3(1,3) + out3(1,11) ; out4(1,4) = out3(1,4) + out3(1,12) ;
    out4(1,5) = out3(1,5) + out3(1,13) ; out4(1,6) = out3(1,6) + out3(1,14) ;
    out4(1,7) = out3(1,7) + out3(1,15) ; out4(1,8) = out3(1,8) + out3(1,16) ;
    out4(1,9) = out3(1,1) - out3(1,9) ; out4(1,10) = out3(1,2) - out3(1,10) ;
    out4(1,11) = out3(1,3) - out3(1,11) ; out4(1,12) = out3(1,4) - out3(1,12) ;
    out4(1,13) = out3(1,5) - out3(1,13) ; out4(1,14) = out3(1,6) - out3(1,14) ;
    out4(1,15) = out3(1,7) - out3(1,15) ; out4(1,16) = out3(1,8) - out3(1,16) ;
    y = out4 ;
end